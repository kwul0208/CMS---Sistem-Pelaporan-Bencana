  <h1>Kebijakan Privasi</h1>
  <p><strong>Terakhir diperbarui:</strong> 20 Juli 2025</p>

  <p>Aplikasi <strong>E-Lapor</strong> merupakan aplikasi pelaporan bencana yang dikembangkan untuk keperluan internal. Kami menghargai privasi Anda dan berkomitmen untuk melindungi data yang Anda berikan kepada kami.</p>

  <h2>1. Informasi yang Kami Kumpulkan</h2>
  <p>Aplikasi E-Lapor hanya mengumpulkan informasi berikut saat digunakan:</p>
  <ul>
    <li><strong>Akses Lokasi (Location):</strong> Digunakan untuk mendapatkan titik koordinat saat pelaporan bencana.</li>
    <li><strong>Akses Internet:</strong> Digunakan untuk mengirim data laporan ke sistem pusat.</li>
  </ul>

  <h2>2. Penggunaan Informasi</h2>
  <p>Informasi yang dikumpulkan hanya digunakan untuk mendukung proses pelaporan bencana secara akurat dan tepat waktu oleh pengguna internal.</p>

  <h2>3. Autentikasi</h2>
  <p>Aplikasi ini tidak menyediakan fitur pendaftaran (registrasi) publik. Hanya pengguna internal yang telah diberikan akses login yang dapat menggunakan aplikasi ini.</p>

  <h2>4. Penyimpanan dan Keamanan Data</h2>
  <p>Data yang dikirim melalui aplikasi akan disimpan secara aman dalam sistem internal kami dan tidak akan dibagikan kepada pihak ketiga tanpa izin.</p>

  <h2>5. Perubahan Kebijakan Privasi</h2>
  <p>Kebijakan privasi ini dapat diperbarui sewaktu-waktu. Perubahan signifikan akan diberitahukan kepada pengguna internal melalui media komunikasi resmi.</p>

  <h2>6. Kontak</h2>
  <p>Jika Anda memiliki pertanyaan atau masukan terkait kebijakan privasi ini, silakan hubungi administrator aplikasi E-Lapor di elapor@gmail.com.</p>

  <p>Dengan menggunakan aplikasi E-Lapor, Anda menyetujui kebijakan privasi ini.</p>
